    private void btnSaveAct(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveAct
        
        int id = Integer.valueOf(inputId.getText());
        String positionName = inputPositionName.getText();
        String positionCode = inputPositionCode.getText();
        
        Map<String, Object> map = new HashMap<String, Object>();
        
        map.put("id", id);
        map.put("name", positionName);
        map.put("code", positionCode);
        
        app.controllers.JabatanController posCtrl = new app.controllers.JabatanController();
    
        boolean res;
        try {
            res = posCtrl.process(map);
        } catch (Exception e) {
            System.err.println(e.getMessage());
            res = false;
        }
        
        if(res == true) {
            this.frame.session.setFlashItem("successNotif", "Jabatan Berhasil Disimpan.");
        } else {
            this.frame.session.setFlashItem("errorNotif", "Gagal Disimpan.");
        }
        
        main = new MainForm(this.frame);
        
        this.frame.setPage(main);
    }//GEN-LAST:event_btnSaveAct


