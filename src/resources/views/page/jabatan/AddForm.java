    private void setData(int id) throws ClassNotFoundException, SQLException {
        System.out.println("ID Edit: " + id);
        
        inputId.setText(String.valueOf(id));
        
        inputPositionCode.setDocument(new JTextFieldLimit(3));
        
        if(id != 0) {
            try {
                app.controllers.JabatanController posCtrl = new app.controllers.JabatanController();
                
                Map<String, Object> map = posCtrl.getData(id);
                
                if(map != null) {
                    inputPositionName.setText((String) map.get("name"));
                    inputPositionCode.setText((String) map.get("code"));
                } else {
                    inputPositionName.setText("");
                    inputPositionCode.setText("");
                }
                
            } catch (Exception e) {
                System.err.println(e.getMessage());
            }
        } else {
            inputPositionName.setText("");
            inputPositionCode.setText("");
        }
    }
    
